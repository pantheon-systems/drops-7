<div class="express-block-designer-description">
  <h2>Block Designer</h2>
  <p>
    The Block Designer is a quick and easy way to design and change the display properties of a block. You can select from configured themes that you can create, or select only the properties that you want to change.
  </p>
  <p>
    A Block Designer Theme is a saved set of Block Designer configurations that you can apply to any block.  Modifying the theme will affect all blocks that have the theme applied.
  </p>
</div>
